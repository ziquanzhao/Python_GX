# + - * /
print(1 + 1)  # 2
print(1 - 1)  # 0
print(2 * 3)  # 6
print(2 / 3)  # 0.66666666

# 取整运算 //  注意一正一负取证要向下取证
print(9 // 2)  # 4
print(9 // -2)  # -5
print(-9 // 2)  # -5
print(-9 // -2)  # 5

# 取余运算 %  取余遵循 余数=被除数-除数*商
print(9 % 2)  # 商4余1
print(-9 % 2)  # 商是-5，余数1
print(9 % -2)  # 商是-5，余数-1
print(-9 % -2)  # 商是4，余数-1

# 幂运算 **
print(2 ** 3)  # 8
