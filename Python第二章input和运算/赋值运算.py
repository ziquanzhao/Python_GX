# 简单赋值 从右往左赋值
a = 10  # 计算机中有储存一个10，然后让a指向10
print(a)

# 链式赋值
a = b = c = 10  # 计算机中储存三个10，分别让a，b，c指向其中一个10
print(a, b, c)

# 解包赋值
a, b, c = 10, 20, 30  # 把10赋给b，把20赋给b，把30赋给c
print(a, b, c)
# 解包赋值作用之一：交换变量
a, b = 10, 20
print('交换之前:', a, b)
a, b = b, a
print('交换之后:', a, b)
