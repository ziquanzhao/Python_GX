#一切对象皆有布尔值

print('----------------以下对象布尔值为False-----------------------')
#查看对象的布尔值用bool()
print(bool(False))  #False的布尔值为False
print(bool(None))  #None
print(bool(0))   #0
print(bool(0.0))  #0.0
print(bool(''))  #空字符串
print(bool(""))   #空字符串
print(bool(()))   #空元组
print(bool([]))   #空列表
print(bool({}))   #空字典



print('----------------其他的对象布尔值都为True------------------------------')